public class LoopWhiles {
    public static void main(String[] args) {
        // while (condition) {
        //     // code block to be executed
        //   }
        int first = 5;
        int second = 10;
        while (first < second) {
            System.out.println(first);
            first = first + 2;
        }
        System.out.println();
        // return 0;
    }
}
